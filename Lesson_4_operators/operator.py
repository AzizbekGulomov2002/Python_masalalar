# Operatorlar turlari:

# 1) Arifmetik operatorlar - barcha turdagi matematik ifodalarni bajarish uchun xizmat qiladigan operator turkumidir

#2) Taqqoslash operatorlari - asosan o`zgaruvchilarga ma`lum qiymat orqali solishtirish vazifasini bajarib beradi. 

#3) Mantiq operatorlari - shartli amallarni birlashtirish uchun ishlatiladigan operatorlar turkumidir. Matiqiy operatorlarni barcha shart operatorlari bilan qo`llash mumkin


#4) Idenfikatsiya operatorlari - ma`lumotlarni xotirada borligini aniqlash uchun va saqlash uchun ishlatiladigan operator turkumidir. 

#5) A'zolik operatorlari - obyektda ketma ketlik mavjudligini tekshirish uchun ishlatiladi. 

